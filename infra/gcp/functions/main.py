def validate_identifier(identifier: str, identifier_type: str = "identifier") -> bool:
    """Validate BigQuery identifiers (dataset, table names)"""
    if not identifier or not isinstance(identifier, str):
        logger.warning(f"Invalid {identifier_type}: not a string")
        return False
    if len(identifier) > 1024:
        logger.warning(f"Invalid {identifier_type}: too long")
        return False
    if not VALID_IDENTIFIER_PATTERN.match(identifier):
        logger.warning(f"Invalid {identifier_type}: invalid characters")
        return False
    return True


def validate_bucket_name(bucket_name: str) -> bool:
    """Validate Cloud Storage bucket name format"""
    if not bucket_name or not isinstance(bucket_name, str):
        logger.warning("Invalid bucket name: not a string")
        return False
    # Bucket names must be 3-63 chars, lowercase, alphanumeric, hyphens
    if not VALID_BUCKET_PATTERN.match(bucket_name):
        logger.warning(f"Invalid bucket name format: {bucket_name[:50]}")
        return False
    return True


def ensure_dataset_and_table(project_id: str) -> str:
    """
    Ensure BigQuery dataset and table exist with proper schema.
    Returns the full table ID.
    """
    dataset_ref = f"{project_id}.{DATASET_ID}"
    table_ref = f"{dataset_ref}.{TABLE_ID}"

    # Check and create dataset if needed
    try:
        bigquery_client.get_dataset(dataset_ref)
        logger.info(f"Dataset {dataset_ref} exists")
    except Exception as e:
        logger.info(f"Creating dataset {dataset_ref}: {e}")
        dataset = bigquery.Dataset(dataset_ref)
        dataset.location = "US"
        bigquery_client.create_dataset(dataset, timeout=30)

    # Check and create table if needed
    try:
        bigquery_client.get_table(table_ref)
        logger.info(f"Table {table_ref} exists")
    except Exception as e:
        logger.info(f"Creating table {table_ref}: {e}")
        table = bigquery.Table(table_ref, schema=SCHEMA)
        table.time_partitioning = bigquery.TimePartitioning(
            type_=bigquery.TimePartitioningType.DAY,
            field="ts",
            expiration_ms=90 * 24 * 3600 * 1000,  # 90 days
        )
        bigquery_client.create_table(table, timeout=30)

    return table_ref


def load_jsonl_to_bigquery(
    bucket_name: str, file_path: str, table_id: str
) -> int:
    """
    Load JSONL file from Cloud Storage to BigQuery with deduplication.

    Dedup strategy (defense-in-depth):
    1. Within the batch: keep only the first occurrence of each event_id.
    2. Against BigQuery: use a MERGE statement keyed on event_id so rows
       that already exist in the table are ignored (WHEN NOT MATCHED INSERT).
    Legacy rows without event_id fall back to streaming insert (no dedup possible).

    Returns the number of rows inserted.
    """
    bucket = storage_client.bucket(bucket_name)
    blob = bucket.blob(file_path)

    logger.info(f"Reading {file_path} from {bucket_name}")

    # Download blob content
    content = blob.download_as_text()

    if not content.strip():
        logger.warning(f"File {file_path} is empty")
        return 0

    # Parse JSONL and prepare rows — deduplicate within batch by event_id
    rows = []
    seen_event_ids: set = set()

    for line_num, line in enumerate(content.strip().split("\n"), 1):
        if not line.strip():
            continue

        try:
            obj = json.loads(line)

            # Validate required fields
            if "ts" not in obj or "event" not in obj:
                logger.warning(f"Line {line_num}: Missing required fields (ts, event)")
                continue

            # Normalize timestamp if needed (ensure RFC 3339 format)
            ts_str = obj.get("ts", "")
            if not ts_str.endswith("Z"):
                ts_str += "Z"

            event_id = obj.get("event_id", "")

            # Batch-level dedup: skip rows with duplicate event_id within this file
            if event_id and event_id in seen_event_ids:
                logger.debug(f"Line {line_num}: Skipping duplicate event_id={event_id}")
                continue
            if event_id:
                seen_event_ids.add(event_id)

            # Build row matching schema — extract _context if present
            raw_ctx = obj.get("_context")
            context = None
            if isinstance(raw_ctx, dict):
                # Map only known schema fields, coerce integer fields
                int_fields = {
                    "files_staged_count", "packages_touched_count",
                    "max_files_threshold", "max_packages_threshold",
                    "commit_msg_length",
                }
                context = {}
                for k, v in raw_ctx.items():
                    if k in int_fields:
                        try:
                            context[k] = int(v)
                        except (TypeError, ValueError):
                            context[k] = None
                    elif isinstance(v, str):
                        context[k] = v[:500]  # cap string fields
                    elif v is None:
                        context[k] = None

            row = {
                "event_id": event_id,
                "ts": ts_str,
                "event": obj.get("event", ""),
                "hook": obj.get("hook", ""),
                "tool": obj.get("tool", ""),
                "result": obj.get("result", ""),
                "reason": obj.get("reason", ""),
                "project": obj.get("project", ""),
                "context": context,
            }
            rows.append(row)

        except json.JSONDecodeError as e:
            logger.warning(f"Line {line_num}: Invalid JSON: {e}")
            continue

    if not rows:
        logger.warning(f"No valid rows found in {file_path}")
        return 0

    # Split into rows with event_id (can use MERGE dedup) and legacy rows (streaming insert)
    # Note: context field is passed through as-is (None or dict) — BigQuery handles RECORD serialization
    rows_with_id = [r for r in rows if r.get("event_id")]
    rows_legacy  = [r for r in rows if not r.get("event_id")]

    inserted = 0

    # ── MERGE path: idempotent upsert keyed on event_id ──────────────────────
    if rows_with_id:
        inserted += _merge_rows_by_event_id(rows_with_id, table_id)

    # ── Legacy path: direct streaming insert (no dedup, best-effort) ─────────
    if rows_legacy:
        logger.info(f"Inserting {len(rows_legacy)} legacy rows (no event_id) via streaming insert")
        errors = bigquery_client.insert_rows_json(table_id, rows_legacy, timeout=30)
        if errors:
            logger.error(f"BigQuery streaming insert errors: {errors}")
        else:
            inserted += len(rows_legacy)

    logger.info(f"Loaded {inserted}/{len(rows)} rows into {table_id}")
    return inserted


def _merge_rows_by_event_id(rows: List[Dict], table_id: str) -> int:
    """
    Insert rows idempotently using streaming insert + dedup-at-query-time strategy.

    Strategy (context RECORD makes inline MERGE SQL impractical):
    1. Filter out rows whose event_id already exists in BigQuery (pre-check query).
    2. Insert only new rows via insert_rows_json (handles RECORD/nested fields natively).
    3. The _dedup_cte() in query layer handles any remaining duplicates at read time.

    For batches > 500 rows, splits into chunks.
    Returns the number of rows inserted.
    """
    BATCH_LIMIT = 500

    if len(rows) > BATCH_LIMIT:
        total = 0
        for i in range(0, len(rows), BATCH_LIMIT):
            total += _merge_rows_by_event_id(rows[i:i + BATCH_LIMIT], table_id)
        return total

    # Pre-check: which event_ids already exist in BigQuery?
    event_ids = [r["event_id"] for r in rows if r.get("event_id")]
    existing_ids: set = set()

    if event_ids:
        try:
            id_list = ", ".join(f"'{eid}'" for eid in event_ids[:500])
            check_sql = f"""
                SELECT event_id FROM `{table_id}`
                WHERE event_id IN ({id_list})
            """
            result = bigquery_client.query(check_sql).result(timeout=30)
            existing_ids = {row["event_id"] for row in result}
            if existing_ids:
                logger.info(f"Pre-check: {len(existing_ids)} event_ids already exist, skipping them")
        except Exception as e:
            logger.warning(f"Pre-check query failed (will insert all): {e}")

    new_rows = [r for r in rows if r.get("event_id") not in existing_ids]

    if not new_rows:
        logger.info("All rows already exist in BigQuery — nothing to insert")
        return 0

    errors = bigquery_client.insert_rows_json(table_id, new_rows, timeout=30)
    if errors:
        logger.error(f"BigQuery insert errors: {errors}")
        return 0

    logger.info(f"Inserted {len(new_rows)}/{len(rows)} rows ({len(existing_ids)} skipped as duplicates)")
    return len(new_rows)


def load_audit_from_gcs(cloud_event):
    """
    Cloud Function entry point (Gen2 / Eventarc CloudEvent format).
    Triggered by: gs://hapai-audit-*/**.jsonl (Eventarc/Cloud Storage)
    """
    # Gen2 Eventarc delivers a CloudEvent object; data is in cloud_event.data
    if hasattr(cloud_event, "data"):
        data = cloud_event.data
    elif isinstance(cloud_event, dict):
        data = cloud_event
    else:
        logger.error(f"Unexpected event type: {type(cloud_event)}")
        return {"status": "error", "message": "Unexpected event format"}

    logger.info(f"Received event data: {json.dumps(data)}")

    # Parse trigger metadata
    bucket_name = data.get("bucket")
    file_path = data.get("name")

    if not bucket_name or not file_path:
        logger.error("Missing bucket or file path in event")
        return {"status": "error", "message": "Invalid event"}

    # Validate file type
    if not file_path.endswith(".jsonl"):
        logger.info(f"Skipping non-JSONL file: {file_path}")
        return {"status": "skipped", "message": "Not a JSONL file"}

    try:
        # Infer project ID from bucket name
        # Bucket naming convention: hapai-audit-{username}
        # GCP project can be inferred from the storage bucket's project
        project_id = bigquery_client.project

        # Ensure dataset and table exist
        table_id = ensure_dataset_and_table(project_id)

        # Load audit log
        rows_loaded = load_jsonl_to_bigquery(bucket_name, file_path, table_id)

        return {
            "status": "success",
            "message": f"Loaded {rows_loaded} rows",
            "bucket": bucket_name,
            "file": file_path,
            "table": table_id,
        }

    except Exception as e:
        logger.error(f"Error processing {file_path}: {str(e)}", exc_info=True)
        return {"status": "error", "message": str(e)}


