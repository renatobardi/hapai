def _validate_period(body: dict) -> int:
    """Validate period param. Returns 7, 14, or 30 (default 30)."""
    period = body.get("period", 30)
    try:
        period = int(period)
    except (TypeError, ValueError):
        return 30
    return period if period in (7, 14, 30) else 30


def _validate_safe_string(value, max_len: int = 100):
    """Validate a string for use as a BigQuery query parameter."""
    if not value or not isinstance(value, str):
        return None
    value = value.strip()
    if not value or len(value) > max_len:
        return None
    return value


def _validate_limit(body: dict, default: int = 100) -> int:
    limit = body.get("limit", default)
    try:
        limit = int(limit)
    except (TypeError, ValueError):
        return default
    return max(10, min(200, limit))


def _validate_offset(body: dict) -> int:
    offset = body.get("offset", 0)
    try:
        offset = int(offset)
    except (TypeError, ValueError):
        return 0
    return max(0, min(10000, offset))


def _run_query(sql: str, params=None) -> list:
    """Run a BigQuery query, optionally with parameterized inputs."""
    job_config = bigquery.QueryJobConfig(query_parameters=params) if params else None
    result = bigquery_client.query(sql, job_config=job_config).result()
    return [dict(row) for row in result]


def _dedup_cte_with_context(ref: str, period: int) -> str:
    """
    Extended dedup CTE that includes the context RECORD for analytics queries.
    """
    return f"""
        WITH deduped AS (
          SELECT
            event_id, ts, event, hook, tool, result, reason, project, context,
            ROW_NUMBER() OVER (
              PARTITION BY COALESCE(NULLIF(event_id, ''), CAST(ts AS STRING) || hook || tool || result)
              ORDER BY ts ASC
            ) AS _rn
          FROM {ref}
          WHERE ts >= TIMESTAMP_SUB(CURRENT_TIMESTAMP(), INTERVAL {period} DAY)
        )
    """


def _dedup_cte(ref: str, period: int) -> str:
    """
    Returns a WITH clause that deduplicates rows by event_id.

    Strategy:
    - Rows WITH event_id: keep only one row per event_id (pick the earliest ts).
      This handles any duplicates that slipped past the MERGE (e.g. legacy rows
      re-ingested before P3 was deployed, or rows from the bq CLI load path).
    - Rows WITHOUT event_id (legacy): deduplicated by composite key (ts, hook, tool, result).
      For each unique combination of these fields, keeps only the earliest row.

    Usage: prefix your query with _dedup_cte(ref, period) and query `deduped` instead
    of the raw table.
    """
    return f"""
        WITH deduped AS (
          SELECT
            event_id, ts, event, hook, tool, result, reason, project,
            ROW_NUMBER() OVER (
              PARTITION BY COALESCE(NULLIF(event_id, ''), CAST(ts AS STRING) || hook || tool || result)
              ORDER BY ts ASC
            ) AS _rn
          FROM {ref}
          WHERE ts >= TIMESTAMP_SUB(CURRENT_TIMESTAMP(), INTERVAL {period} DAY)
        )
    """


def _query_stats(period: int) -> list:
    ref = _get_table_ref(TABLE_ID)
    sql = f"""
        {_dedup_cte(ref, period)}
        SELECT
          COUNTIF(result = 'deny')        AS denials,
          COUNTIF(result = 'warn')        AS warnings,
          COUNTIF(result = 'allow')       AS allow_count,
          CAST(COUNT(*) AS INT64)         AS total_events
        FROM deduped
        WHERE _rn = 1
    """
    return _run_query(sql)


def _query_timeline(period: int) -> list:
    ref = _get_table_ref(TABLE_ID)
    sql = f"""
        {_dedup_cte(ref, period)}
        SELECT
          FORMAT_DATE('%Y-%m-%d', DATE(ts)) AS day,
          result                            AS event,
          CAST(COUNT(*) AS INT64)           AS count
        FROM deduped
        WHERE _rn = 1
          AND result IN ('allow', 'deny', 'warn')
        GROUP BY day, result
        ORDER BY day
    """
    return _run_query(sql)


