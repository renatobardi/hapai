def _query_denials(
    limit: int,
    offset: int,
    event_filter: str = None,
    hook_filter: str = None,
    tool_filter: str = None,
) -> list:
    ref = _get_table_ref(TABLE_ID)
    # _dedup_cte already filters by period; for denials we use a large window (90d)
    inner_where = ["result IN ('deny', 'warn')", "_rn = 1"]
    params: List[Any] = []

    if event_filter:
        inner_where.append("result = @event_filter")
        params.append(bigquery.ScalarQueryParameter("event_filter", "STRING", event_filter))
    if hook_filter:
        inner_where.append("hook = @hook_filter")
        params.append(bigquery.ScalarQueryParameter("hook_filter", "STRING", hook_filter))
    if tool_filter:
        inner_where.append("tool = @tool_filter")
        params.append(bigquery.ScalarQueryParameter("tool_filter", "STRING", tool_filter))

    params.append(bigquery.ScalarQueryParameter("q_limit",  "INT64", limit))
    params.append(bigquery.ScalarQueryParameter("q_offset", "INT64", offset))

    sql = f"""
        {_dedup_cte(ref, 90)}
        SELECT
          FORMAT_TIMESTAMP('%Y-%m-%dT%H:%M:%SZ', ts) AS ts,
          event, hook, tool, result, project
        FROM deduped
        WHERE {" AND ".join(inner_where)}
        ORDER BY ts DESC
        LIMIT @q_limit
        OFFSET @q_offset
    """
    return _run_query(sql, params)


def _query_project_health(period: int) -> list:
    """
    Per-project health: total events, denial rate %, warning rate %, trend.
    Returns projects sorted by denial rate descending.
    """
    ref = _get_table_ref(TABLE_ID)
    sql = f"""
        {_dedup_cte(ref, period)}
        SELECT
          project,
          CAST(COUNT(*) AS INT64)                              AS total_events,
          CAST(COUNTIF(result = 'deny') AS INT64)              AS denials,
          CAST(COUNTIF(result = 'warn') AS INT64)              AS warnings,
          CAST(COUNTIF(result = 'allow') AS INT64)             AS allows,
          ROUND(SAFE_DIVIDE(COUNTIF(result = 'deny') * 100.0, COUNT(*)), 1) AS denial_rate,
          ROUND(SAFE_DIVIDE(COUNTIF(result = 'warn') * 100.0, COUNT(*)), 1) AS warning_rate,
          MIN(ts) AS first_seen,
          MAX(ts) AS last_seen
        FROM deduped
        WHERE _rn = 1
          AND project IS NOT NULL
          AND result IN ('deny', 'warn', 'allow')
        GROUP BY project
        HAVING total_events >= 5
        ORDER BY denial_rate DESC
        LIMIT 20
    """
    return _run_query(sql)


def _query_denial_reasons(period: int, hook_filter: str = None) -> list:
    """
    Aggregate denial reasons — most common messages, grouped by hook and reason text.
    Reveals patterns like 'Commit blocked on protected branch main' appearing 80 times.
    """
    ref = _get_table_ref(TABLE_ID)
    where = ["_rn = 1", "result = 'deny'", "reason IS NOT NULL", "reason != ''"]
    params = []

    if hook_filter:
        where.append("hook = @hook_filter")
        params.append(bigquery.ScalarQueryParameter("hook_filter", "STRING", hook_filter))

    sql = f"""
        {_dedup_cte(ref, period)}
        SELECT
          hook,
          reason,
          CAST(COUNT(*) AS INT64) AS count
        FROM deduped
        WHERE {" AND ".join(where)}
        GROUP BY hook, reason
        ORDER BY count DESC
        LIMIT 30
    """
    return _run_query(sql, params if params else None)


def _query_context_breakdown(period: int, hook_filter: str = None) -> dict:
    """
    Context-aware analytics: breakdowns using structured _context fields.
    Returns per-hook breakdowns by category (file_category, risk_category, target_branch, etc.)
    """
    ref = _get_table_ref(TABLE_ID)
    cte = _dedup_cte_with_context(ref, period)
    hook_clause = ""
    params = []

    if hook_filter:
        hook_clause = "AND hook = @hook_filter"
        params.append(bigquery.ScalarQueryParameter("hook_filter", "STRING", hook_filter))

    # File categories blocked
    file_cats = _run_query(f"""
        {cte}
        SELECT
          context.file_category AS category,
          CAST(COUNT(*) AS INT64) AS count
        FROM deduped
        WHERE _rn = 1
          AND result = 'deny'
          AND context.file_category IS NOT NULL
          {hook_clause}
        GROUP BY category ORDER BY count DESC
    """, params if params else None)

    # Risk categories (destructive)
    risk_cats = _run_query(f"""
        {cte}
        SELECT
          context.risk_category AS category,
          CAST(COUNT(*) AS INT64) AS count
        FROM deduped
        WHERE _rn = 1
          AND result = 'deny'
          AND context.risk_category IS NOT NULL
          {hook_clause}
        GROUP BY category ORDER BY count DESC
    """, params if params else None)

    # Target branches blocked
    branches = _run_query(f"""
        {cte}
        SELECT
          context.target_branch AS branch,
          CAST(COUNT(*) AS INT64) AS count
        FROM deduped
        WHERE _rn = 1
          AND result = 'deny'
          AND context.target_branch IS NOT NULL
          {hook_clause}
        GROUP BY branch ORDER BY count DESC LIMIT 10
    """, params if params else None)

    # Git operations blocked
    git_ops = _run_query(f"""
        {cte}
        SELECT
          context.git_op AS op,
          CAST(COUNT(*) AS INT64) AS count
        FROM deduped
        WHERE _rn = 1
          AND result = 'deny'
          AND context.git_op IS NOT NULL
          {hook_clause}
        GROUP BY op ORDER BY count DESC
    """, params if params else None)

    # Forbidden patterns (commit-msg)
    commit_patterns = _run_query(f"""
        {cte}
        SELECT
          context.forbidden_pattern AS pattern,
          CAST(COUNT(*) AS INT64) AS count
        FROM deduped
        WHERE _rn = 1
          AND result = 'deny'
          AND context.forbidden_pattern IS NOT NULL
          {hook_clause}
        GROUP BY pattern ORDER BY count DESC LIMIT 10
    """, params if params else None)

    return {
        "file_categories": file_cats,
        "risk_categories": risk_cats,
        "blocked_branches": branches,
        "git_ops": git_ops,
        "commit_patterns": commit_patterns,
    }


def _query_stats_comparison(period: int) -> list:
    """
    Stats for current period AND previous period of equal length — enables trend vs. previous period.
    Returns two rows: current and previous.
    """
    ref = _get_table_ref(TABLE_ID)
    sql = f"""
        WITH all_events AS (
          SELECT
            event_id, ts, hook, tool, result,
            ROW_NUMBER() OVER (
              PARTITION BY COALESCE(NULLIF(event_id, ''), CAST(ts AS STRING) || hook || tool || result)
              ORDER BY ts ASC
            ) AS _rn
          FROM {ref}
          WHERE ts >= TIMESTAMP_SUB(CURRENT_TIMESTAMP(), INTERVAL {period * 2} DAY)
        ),
        deduped AS (SELECT * FROM all_events WHERE _rn = 1)
        SELECT
          CASE
            WHEN ts >= TIMESTAMP_SUB(CURRENT_TIMESTAMP(), INTERVAL {period} DAY)
            THEN 'current'
            ELSE 'previous'
          END AS period_label,
          CAST(COUNT(*) AS INT64)                                        AS total_events,
          CAST(COUNTIF(result = 'deny') AS INT64)                        AS denials,
          CAST(COUNTIF(result = 'warn') AS INT64)                        AS warnings,
          ROUND(SAFE_DIVIDE(COUNTIF(result = 'deny') * 100.0, COUNT(*)), 1) AS denial_rate,
          ROUND(SAFE_DIVIDE(COUNTIF(result = 'warn') * 100.0, COUNT(*)), 1) AS warning_rate
        FROM deduped
        WHERE result IN ('deny', 'warn', 'allow')
        GROUP BY period_label
    """
    return _run_query(sql)


def _query_hook_detail(hook_name: str, period: int) -> dict:
    """Mini-timeline + tool breakdown + recent events for a specific guard."""
    ref = _get_table_ref(TABLE_ID)
    p = [bigquery.ScalarQueryParameter("hook_name", "STRING", hook_name)]
    cte = _dedup_cte(ref, period)

    timeline = _run_query(f"""
        {cte}
        SELECT
          FORMAT_DATE('%Y-%m-%d', DATE(ts)) AS day,
          CAST(COUNT(*) AS INT64)           AS count
        FROM deduped
        WHERE _rn = 1
          AND hook = @hook_name
          AND result IN ('deny', 'warn')
        GROUP BY day ORDER BY day
    """, p)

    breakdown = _run_query(f"""
        {cte}
        SELECT
          tool                    AS label,
          CAST(COUNT(*) AS INT64) AS count
        FROM deduped
        WHERE _rn = 1
          AND hook = @hook_name
          AND result IN ('deny', 'warn')
        GROUP BY tool ORDER BY count DESC LIMIT 8
    """, p)

    recent = _run_query(f"""
        {cte}
        SELECT
          FORMAT_TIMESTAMP('%Y-%m-%dT%H:%M:%SZ', ts) AS ts,
          event, hook, tool, result, project
        FROM deduped
        WHERE _rn = 1
          AND hook = @hook_name
          AND result IN ('deny', 'warn')
        ORDER BY ts DESC LIMIT 10
    """, p)

    stats_row = _run_query(f"""
        {cte}
        SELECT
          CAST(COUNT(*) AS INT64)                 AS total,
          CAST(COUNTIF(result = 'deny') AS INT64) AS deny_count,
          CAST(COUNTIF(result = 'warn') AS INT64) AS warn_count
        FROM deduped
        WHERE _rn = 1
          AND hook = @hook_name
          AND result IN ('deny', 'warn')
    """, p)

    s = stats_row[0] if stats_row else {"total": 0, "deny_count": 0, "warn_count": 0}
    return {"timeline": timeline, "breakdown": breakdown, "recent": recent, **s}


